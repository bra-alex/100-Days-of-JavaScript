//Date Objects
//Creating time object
const now = new Date()
console.log(now)
console.log(now.getFullYear())//Get Year
console.log(now.getMonth())//Get Month
console.log(now.getDate())//Get Date
console.log(now.getDay())//Get Day
console.log(now.getHours())//Get Hours
console.log(now.getMinutes())//Get Minutes
console.log(now.getSeconds())//Get Seconds
console.log(now.getMilliseconds())//Get Milliseconds
console.log(now.getTime())//Get Time (Milliseconds since January 1970)




